


class Person:
    def __init__(self,
                name: str = None,
                age: str = None,
                address: str = None,
                city_and_state: str = None,
                phone_number: str = None,
                email: str = None,
                birth_date: str = None,
                linkedin: str = None):
        self.name = name
        self.age = age
        self.address = address
        self.city_and_state = city_and_state
        self.phone_number = phone_number
        self.email = email
        self.birth_date = birth_date
        self.linkedin = linkedin
    
    